package com.practice.test.market.entity;

import jakarta.persistence.*;

@Entity
@Table(name="ordered_item_status")
public class OrderedItemStatus {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "id")
    private long id;

    @Column(name = "type")
    private String type;

    public OrderedItemStatus(){}

    public OrderedItemStatus(String type) {
        this.type = type;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
